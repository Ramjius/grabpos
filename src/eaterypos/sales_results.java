    private void FetchSalesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FetchSalesActionPerformed
        // Clear fields if necessary (e.g., totals and other UI elements)
    clearFields();

    // Formatting the date range
    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
    String jdStr = sdf.format(sdateSales.getDate());
    String jd1Str = sdf.format(edateSales.getDate());

    PreparedStatement pstmt = null;
    PreparedStatement pstmt1 = null;
    ResultSet rs = null;
    ResultSet rs1 = null;

    try {
        Con = DriverManager.getConnection("jdbc:mariadb://localhost:3306/grabdb", "root", "admin");

        // Prepare the SQL query with placeholders to avoid SQL injection
        String sql = "SELECT * FROM orders WHERE date >= ? AND date <= ?";
        String sql1 = "SELECT COUNT(*) AS total_count FROM orders WHERE date >= ? AND date <= ?";

        // Creating the PreparedStatement
        pstmt = Con.prepareStatement(sql);
        pstmt1 = Con.prepareStatement(sql1);

        // Setting the date parameters
        pstmt.setString(1, jdStr);
        pstmt.setString(2, jd1Str);
        pstmt1.setString(1, jdStr);
        pstmt1.setString(2, jd1Str);

        // Execute the query
        rs = pstmt.executeQuery();
        
        // Execute the query to get the count first
        rs1 = pstmt1.executeQuery();
        if (rs1.next()) {
            int count = rs1.getInt("total_count");
            // Set the result in the JTextField
            ordersCount.setText(String.valueOf(count));
        } else {
            ordersCount.setText("0");
        }

        // Create a DefaultTableModel to manipulate data
        DefaultTableModel model = (DefaultTableModel) DbUtils.resultSetToTableModel(rs);

        // Calculate the sum of the total column
        int totalGrandSum = 0;
        int totalPaid = 0;
        for (int i = 0; i < model.getRowCount(); i++) {
            totalGrandSum += Integer.parseInt(model.getValueAt(i, 2).toString()); // Assuming the 'grand_total' column is at index 3
            totalPaid += Integer.parseInt(model.getValueAt(i, 3).toString()); // Assuming the 'amount_paid' column is at index 4
        }
        
        // Add a new row with the sum of the total column
        Object[] sumRow = {"Total", "", totalGrandSum, totalPaid, "", ""}; // Adjust indices as per your table structure
        model.addRow(sumRow);

        // Set the model to the table
        SalesTable.setModel(model);

        // Update other UI elements
        grand_total.setText(String.valueOf(totalGrandSum));
        
        // Get the text from the period name JTextField
        String periodNameText = periodTxt.getText();
        // Set the text of the periodName JTextField to the periodTxt
        periodName.setText(periodNameText);

        // Get the text from the username JTextField
        String usernameText = username.getText();
        // Set the text of the usernameSummary JTextField to the usernameText
        usernameSales.setText(usernameText);

        // Get the current date and time
        LocalDateTime now = LocalDateTime.now();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd-MM-yyyy HH:mm:ss");
        String formattedDateTime = now.format(formatter);

        // Set the current date and time
        currDate.setText(formattedDateTime);

        // Set start and end dates in summary fields
        sdateTxt.setText(jdStr);
        edateTxt.setText(jd1Str);

        } catch (SQLException e) {
            // Properly log the exception and show an error message to the user
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, "Error fetching order items", e);
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            // Close resources
            if (rs != null) {
                try {
                    rs.close();
                } catch (SQLException ex) {
                    // Handle close exception if necessary
                }
            }

            if (pstmt != null) {
                try {
                    pstmt.close();
                } catch (SQLException ex) {
                    // Handle close exception if necessary
                }
            }

            if (Con != null) {
                try {
                    Con.close();
                } catch (SQLException ex) {
                    // Handle close exception if necessary
                }
            }
        }
    }//GEN-LAST:event_FetchSalesActionPerformed

