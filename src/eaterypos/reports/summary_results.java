    private void FetchSalesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FetchSalesActionPerformed
        
        clearFields();
        
        // Formatting the date range
    SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy HH:mm:ss");
    String jdStr = sdf.format(sdateSales.getDate());
    String jd1Str = sdf.format(edateSales.getDate());
    PreparedStatement pstmt = null;
    PreparedStatement pstmt1 = null;
    PreparedStatement pstmt2 = null;
    PreparedStatement pstmt3 = null;
    PreparedStatement pstmt4 = null;
    PreparedStatement pstmt5 = null;
    ResultSet rs = null;
    ResultSet rs1 = null;
    ResultSet rs2 = null;
    ResultSet rs3 = null;
    ResultSet rs4 = null;
    ResultSet rs5 = null;

    try {
        Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/grabdb", "root", "admin");

        // Prepare the SQL queries with placeholders to avoid SQL injection
        String sql = "SELECT * FROM orders WHERE order_date_time >= ? AND order_date_time <= ?";
        String sql1 = "SELECT COUNT(*) AS total_count FROM orders WHERE order_date_time >= ? AND order_date_time <= ?";
        String sql2 = "SELECT item_name, COUNT(*) as item_count FROM order_items WHERE date >= ? AND date <= ? GROUP BY item_name ORDER BY item_count DESC LIMIT 1";
        String sql3 = "SELECT SUM(item_quantity) AS total_quantity FROM order_items WHERE item_name = ? AND date >= ? AND date <= ?";
        String sql4 = "SELECT * FROM expenses WHERE date >= ? AND date <= ?";
        String sql5 = "SELECT item_name, COUNT(*) as expense_count FROM expenses WHERE date >= ? AND date <= ? GROUP BY item_name ORDER BY expense_count DESC LIMIT 1";

        // Creating the PreparedStatements
        pstmt = Con.prepareStatement(sql);
        pstmt1 = Con.prepareStatement(sql1);
        pstmt2 = Con.prepareStatement(sql2);
        pstmt3 = Con.prepareStatement(sql3);
        pstmt4 = Con.prepareStatement(sql4);
        pstmt5 = Con.prepareStatement(sql5);

        // Setting the date parameters for all queries
        pstmt.setString(1, jdStr);
        pstmt.setString(2, jd1Str);
        pstmt1.setString(1, jdStr);
        pstmt1.setString(2, jd1Str);
        pstmt2.setString(1, jdStr);
        pstmt2.setString(2, jd1Str);
        pstmt4.setString(1, jdStr);
        pstmt4.setString(2, jd1Str);
        pstmt5.setString(1, jdStr);
        pstmt5.setString(2, jd1Str);

        // Execute the query to get the count first
        rs1 = pstmt1.executeQuery();
        if (rs1.next()) {
            int count = rs1.getInt("total_count");
            // Set the result in the JTextField
            salesCount.setText(String.valueOf(count));
        } else {
            salesCount.setText("0");
        }

        // Execute the query to get the most sold item
        rs2 = pstmt2.executeQuery();
        String mostSoldItem = null;
        if (rs2.next()) {
            mostSoldItem = rs2.getString("item_name");
            // Set the result in the JTextField
            item_name.setText(mostSoldItem);
        } else {
            item_name.setText("No Data");
        }

        if (mostSoldItem != null) {
            pstmt3.setString(1, mostSoldItem);
            pstmt3.setString(2, jdStr);
            pstmt3.setString(3, jd1Str);

            // Execute the query to get the total quantity of the most sold item
            rs3 = pstmt3.executeQuery();
            if (rs3.next()) {
                int totalQuantity = rs3.getInt("total_quantity");
                // Set the result in the JTextField
                item_count.setText(String.valueOf(totalQuantity));
            } else {
                item_count.setText("0");
            }
        }

        // Execute the main query to get the orders
        rs = pstmt.executeQuery();
        rs4 = pstmt4.executeQuery();

        // Execute the query to get the most expensive item
        rs5 = pstmt5.executeQuery();
        String mostExpense = null;
        if (rs5.next()) {
            mostExpense = rs5.getString("item_name");
            // Set the result in the JTextField
            expense_name.setText(mostExpense);
        } else {
            expense_name.setText("No Data");
        }

        // Create a DefaultTableModel to manipulate data
        DefaultTableModel model = (DefaultTableModel) DbUtils.resultSetToTableModel(rs);
        DefaultTableModel model1 = (DefaultTableModel) DbUtils.resultSetToTableModel(rs4);

        // Calculate the sum of the total column
        int totalGrandSum = 0;
        for (int i = 0; i < model.getRowCount(); i++) {
            totalGrandSum += Integer.parseInt(model.getValueAt(i, 2).toString()); // Assuming the 'total' column is at index 2
        }

        // Calculate the sum of the price column in the expenses table
        int expenseTotal = 0;
        for (int i = 0; i < model1.getRowCount(); i++) {
            expenseTotal += Integer.parseInt(model1.getValueAt(i, 5).toString()); // Assuming the 'price' column is at index 5
        }

        // Set the text of the grand_total JTextField to the value of totalGrandSum
        grand_total.setText(String.valueOf(totalGrandSum));
        // Set the text of the total_expense JTextField to the value of expenseTotal
        total_expense.setText(String.valueOf(expenseTotal));

        // Get the current date and time
        LocalDateTime now = LocalDateTime.now();
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");
        String formattedDateTime = now.format(formatter);

        // Set the current date and time
        currDate.setText(formattedDateTime);

        // Set start and end dates in summary fields
        sdateSummary.setText(jdStr);
        edateSummary.setText(jd1Str);

        // Get the text from the username JTextField
        String usernameText = username.getText();
        // Set the text of the usernameSummary JTextField to the usernameText
        usernameSummary.setText(usernameText);

        } catch (SQLException e) {
            // Properly log the exception and show an error message to the user
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, "Error fetching order items", e);
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            // Close resources
            closeResultSet(rs5);
            closeResultSet(rs4);
            closeResultSet(rs3);
            closeResultSet(rs2);
            closeResultSet(rs1);
            closeResultSet(rs);
            closePreparedStatement(pstmt5);
            closePreparedStatement(pstmt4);
            closePreparedStatement(pstmt3);
            closePreparedStatement(pstmt2);
            closePreparedStatement(pstmt1);
            closePreparedStatement(pstmt);
            closeConnection(Con);
        }
    }//GEN-LAST:event_FetchSalesActionPerformed
    
